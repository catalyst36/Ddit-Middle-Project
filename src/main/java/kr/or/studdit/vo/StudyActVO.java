package kr.or.studdit.vo;

public class StudyActVO {
	private String act_code;
	private String act_tit;
	private String act_con;
	private String bor_code;
	private String act_date;
	public String getAct_code() {
		return act_code;
	}
	public void setAct_code(String act_code) {
		this.act_code = act_code;
	}
	public String getAct_tit() {
		return act_tit;
	}
	public void setAct_tit(String act_tit) {
		this.act_tit = act_tit;
	}
	public String getAct_con() {
		return act_con;
	}
	public void setAct_con(String act_con) {
		this.act_con = act_con;
	}
	public String getBor_code() {
		return bor_code;
	}
	public void setBor_code(String bor_code) {
		this.bor_code = bor_code;
	}
	public String getAct_date() {
		return act_date;
	}
	public void setAct_date(String act_date) {
		this.act_date = act_date;
	}
}
