package kr.or.studdit.vo;

// 파일
public class FileVO {
	private String fil_num;
	private String fil_name;
	private String fil_cha;
	private String fil_ext;
	private String fil_size;
	private String fil_not;
	private int fil_san;
	private String bor_code;
	private String cmt_code;
	private String std_id;
	
	public String getFil_num() {
		return fil_num;
	}
	public void setFil_num(String fil_num) {
		this.fil_num = fil_num;
	}
	public String getFil_name() {
		return fil_name;
	}
	public void setFil_name(String fil_name) {
		this.fil_name = fil_name;
	}
	public String getFil_cha() {
		return fil_cha;
	}
	public void setFil_cha(String fil_cha) {
		this.fil_cha = fil_cha;
	}
	public String getFil_ext() {
		return fil_ext;
	}
	public void setFil_ext(String fil_ext) {
		this.fil_ext = fil_ext;
	}
	public String getFil_size() {
		return fil_size;
	}
	public void setFil_size(String fil_size) {
		this.fil_size = fil_size;
	}
	public String getFil_not() {
		return fil_not;
	}
	public void setFil_not(String fil_not) {
		this.fil_not = fil_not;
	}
	public int getFil_san() {
		return fil_san;
	}
	public void setFil_san(int fil_san) {
		this.fil_san = fil_san;
	}
	public String getBor_code() {
		return bor_code;
	}
	public void setBor_code(String bor_code) {
		this.bor_code = bor_code;
	}
	public String getCmt_code() {
		return cmt_code;
	}
	public void setCmt_code(String cmt_code) {
		this.cmt_code = cmt_code;
	}
	public String getStd_id() {
		return std_id;
	}
	public void setStd_id(String std_id) {
		this.std_id = std_id;
	}
	
}
