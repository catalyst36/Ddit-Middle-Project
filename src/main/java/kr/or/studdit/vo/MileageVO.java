package kr.or.studdit.vo;

// 마일리지
public class MileageVO {
	private String mil_code;
	private int mul_grn;
	private String mic_code;
	private String std_id;
	private String cmt_code;
	private String bor_code;
	private String stm_day;
	
	public String getMil_code() {
		return mil_code;
	}
	public void setMil_code(String mil_code) {
		this.mil_code = mil_code;
	}
	public int getMul_grn() {
		return mul_grn;
	}
	public void setMul_grn(int mul_grn) {
		this.mul_grn = mul_grn;
	}
	public String getMic_code() {
		return mic_code;
	}
	public void setMic_code(String mic_code) {
		this.mic_code = mic_code;
	}
	public String getStd_id() {
		return std_id;
	}
	public void setStd_id(String std_id) {
		this.std_id = std_id;
	}
	public String getCmt_code() {
		return cmt_code;
	}
	public void setCmt_code(String cmt_code) {
		this.cmt_code = cmt_code;
	}
	public String getBor_code() {
		return bor_code;
	}
	public void setBor_code(String bor_code) {
		this.bor_code = bor_code;
	}
	public String getStm_day() {
		return stm_day;
	}
	public void setStm_day(String stm_day) {
		this.stm_day = stm_day;
	}
	
}
