// 마일리지 분류
public class MileageClassVO {
	private String mic_code;
	private String mic_mcn;
	private int mic_mil;
	
	public String getMic_code() {
		return mic_code;
	}
	public void setMic_code(String mic_code) {
		this.mic_code = mic_code;
	}
	public String getMic_mcn() {
		return mic_mcn;
	}
	public void setMic_mcn(String mic_mcn) {
		this.mic_mcn = mic_mcn;
	}
	public int getMic_mil() {
		return mic_mil;
	}
	public void setMic_mil(int mic_mil) {
		this.mic_mil = mic_mil;
	}
	
}
